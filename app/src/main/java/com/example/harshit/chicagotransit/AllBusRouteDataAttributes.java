public class AllBusRouteDataAttributes {
    private String rootNo;
    private String rootName;
    private String rootClr;


    public String getRootNo() {
        return rootNo;
    }

    public void setRootNo(String rootNo) {
        this.rootNo = rootNo;
    }

    public String getRootName() {
        return rootName;
    }

    public void setRootName(String rootName) {
        this.rootName = rootName;
    }

    public String getRootClr() {
        return rootClr;
    }

    public void setRootClr(String rootClr) {
        this.rootClr = rootClr;
    }
}
